

def main():
    book_path = "/Users/fljalufka/workspace/github.com/frankjalufka/bookbot/books/frankenstein.txt"
    text = get_book_text(book_path)
    num_words = get_num_words(text)
    print(f"{num_words} words found in the document")

def get_num_words(text):
    words = text.split()
    return len(words)

def get_book_text(path):
    with open(path) as f:
        return f.read()

if __name__ =='__main__':
    main()
